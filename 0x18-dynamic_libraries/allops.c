#include <stdio.h>
/**
 * add - adding two ints
 * @a: int 1
 * @b: int 2
 * return: addition
 */
int add(int a, int b)
{
	return(a + b);
}

/**
 * sub - subtracting two ints
 *
 * return: result
 */
int sub(int a, int b)
{
return(a - b);
}

/**
 * div - dividing two ints
 *
 * return: result
 */
int div(int a, int b)
{
return(a / b);
}

/**
 * mod - mod of two ints
 *
 * return: remainder
 */
int mod(int a, int b)
{
return(a % b);
}
/**
 * mul - multiplying two ints
 *
 * return: result
 */
int mul(int a, int b)
{
return(a * b);
}
